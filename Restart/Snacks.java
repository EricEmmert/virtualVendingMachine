package Restart;

public class Snacks {
    private String location;
    private String name;
    private double price;
    private String type;
    private String sound;

    public Snacks(String location, String name, double price, String type, String sound) {
        this.location = location;
        this.name = name;
        this.price = price;
        this.type = type;
        this.sound = sound;

    }
}

